//datatypes (size in bytes ).
#include <iostream>
using namespace std ;
int main()
{
	int a;
	cout<<"sizeof int       ="<<sizeof(a)<<endl;
	
	float b;
	cout<<"sizeof flot      ="<<sizeof(b)<<endl;
	
	char c;
	cout<<"sizeof char      ="<<sizeof(c)<<endl;
	
	double d;
	cout<<"sizeof double    ="<<sizeof(d)<<endl;
	
	long long int ln;
	cout<<"sizeof long  int ="<<sizeof(ln)<<endl; 	
	
	short int sn ;
	cout<<"sizeof short int ="<<sizeof(sn)<<endl;
	
	bool bo;
	cout<<"sizeof bool      ="<<sizeof(bo)<<endl;


	
	return 0;
}
